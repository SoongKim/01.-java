public class S06_SevenToNine_06 {

	public static void main(String[] args) {

		int mainSeven = 7;
		int mainEight = 8;
		int mainNine = 9;

		for (int i = 1; i < 10; i++) {
			System.out.println("7 X " + i + " = " + mainSeven * i);
		}

		System.out.println("-----------------------------------");

		for (int i = 1; i < 10; i++) {
			System.out.println("8 X " + i + " = " + mainEight * i);
		}

		System.out.println("-----------------------------------");

		for (int i = 1; i < 10; i++) {
			System.out.println("9 X " + i + " = " + mainNine * i);
		}

		System.out.println("-----------------------------------");
	}

}