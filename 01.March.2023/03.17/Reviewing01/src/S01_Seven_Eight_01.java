public class S01_Seven_Eight_01 {

	public static void main(String[] args) {

		int mainSeven = 7;
		int mainEight = 8;

		for (int i = 1; i < 10; i++) {
			System.out.println("7 X " + i + " = " + mainSeven * i);
		}
		for (int i = 1; i < 10; i++) {
			System.out.println("8 X " + i + " = " + mainEight * i);
		}
	}

}